package day_18_for_loop;

public class StartEnd {
    public static void main(String[] args) {
        //print 6 to 13
        int start = 6;
        int end = 9;
        for (int i = start; start <= end; start++) {
            System.out.println(start);
        }
        for (int i = 10; i >= 0; i--) {
            System.out.print(i);
        }
        }
    }

