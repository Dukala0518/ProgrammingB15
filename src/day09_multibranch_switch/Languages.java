package day09_multibranch_switch;

public class Languages {
    public static void main(String[] args){
        int option=2;
        if(option==1){
            System.out.println("Hello!");
        } else if(option==2) {
            System.out.println("Hola!");
        } else if(option==3){
            System.out.println("Konnichiwa");
        } else{
            System.out.println("Bye");
        }

    }
}
