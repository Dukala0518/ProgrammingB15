package day10_switch_logical_operators;

public class LogicalOperators {
    public static void main(String[] args) {
        int apples = 10;
        int oranges = 20;
        if (apples > 10 && oranges > 20) {
            System.out.println("True:");
        } else {
            System.out.println("False");
        }
    }

}